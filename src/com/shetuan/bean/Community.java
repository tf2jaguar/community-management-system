package com.shetuan.bean;

public class Community {
	//社团ID
	private int communityId;
	//社团名
	private String communityName;
	//社团创建者ID
	private int communityCreaterId;
	//社团创建者名字
	private String communityCreaterName;
	//社团创建时间
	private String communityCreateDate;
	//社团人数
	private int communityNum;
	//社团范围
	private String communityRange;
	//社团类别ID
	private int communityClassId;
	//社团类别名字
	private String communityClassName;
	//社团是否创建
	private int communityCreate;
	//社团详细信息
	private String communityInfo;
	
	public String getCommunityCreaterName() {
		return communityCreaterName;
	}
	public void setCommunityCreaterName(String communityCreaterName) {
		this.communityCreaterName = communityCreaterName;
	}
	public String getCommunityClassName() {
		return communityClassName;
	}
	public void setCommunityClassName(String communityClassName) {
		this.communityClassName = communityClassName;
	}
	public String getCommunityInfo() {
		return communityInfo;
	}
	public void setCommunityInfo(String communityInfo) {
		this.communityInfo = communityInfo;
	}
	public int getCommunityId() {
		return communityId;
	}
	public void setCommunityId(int communityId) {
		this.communityId = communityId;
	}
	public String getCommunityName() {
		return communityName;
	}
	public void setCommunityName(String communityName) {
		this.communityName = communityName;
	}
	public int getCommunityCreaterId() {
		return communityCreaterId;
	}
	public void setCommunityCreaterId(int communityCreaterId) {
		this.communityCreaterId = communityCreaterId;
	}
	
	public String getCommunityCreateDate() {
		return communityCreateDate;
	}
	public void setCommunityCreateDate(String communityCreateDate) {
		this.communityCreateDate = communityCreateDate;
	}
	public int getCommunityNum() {
		return communityNum;
	}
	public void setCommunityNum(int communityNum) {
		this.communityNum = communityNum;
	}
	public String getCommunityRange() {
		return communityRange;
	}
	public void setCommunityRange(String communityRange) {
		this.communityRange = communityRange;
	}
	public int getCommunityClassId() {
		return communityClassId;
	}
	public void setCommunityClassId(int communityClassId) {
		this.communityClassId = communityClassId;
	}
	public int getCommunityCreate() {
		return communityCreate;
	}
	public void setCommunityCreate(int communityCreate) {
		this.communityCreate = communityCreate;
	}

	
}
